  id bigserial primary key,
  municipio_norm text not null,
  municipio text not null,
  regiao text,
  categoria text not null,
  ano int not null,
  preco_deral numeric not null,
  indice numeric not null,
  unique (municipio_norm, categoria)
);
create index if not exists deral_ref_muni_ix on public.deral_ref (municipio_norm);
alter table public.deral_ref enable row level security;
drop policy if exists deral_ref_read on public.deral_ref;
create policy deral_ref_read on public.deral_ref for select to anon, authenticated using (true);

